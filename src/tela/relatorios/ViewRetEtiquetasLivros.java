    private void Pesquisar() {
        try {
            ArrayList<Livro> lista = livroDAO.consultaPersonalizada2(cbPesquisa.getSelectedIndex(), txtPesq.getText().trim());
            modelTable = new EtiquetasTableModel(lista);
            jTable1.setModel(modelTable);
            ajusta_tamanho_coluna(jTable1);
        } catch (Exception ex) {
            LoggerUtil.log(ViewRetEtiquetasLivros.class,ex.getMessage());
            JOptionPane.showMessageDialog(this, "Erro ao carregar grade.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void ajusta_tamanho_coluna(JTable table) {
        table.getColumnModel().getColumn(0).setPreferredWidth(30);
        table.getColumnModel().getColumn(1).setPreferredWidth(120);
        table.getColumnModel().getColumn(2).setPreferredWidth(300);
        table.getColumnModel().getColumn(3).setPreferredWidth(100);
        table.getColumnModel().getColumn(4).setPreferredWidth(300);
        table.getColumnModel().getColumn(5).setPreferredWidth(250);
        table.getColumnModel().getColumn(6).setPreferredWidth(300);
        table.getColumnModel().getColumn(7).setPreferredWidth(300);
        table.getColumnModel().getColumn(8).setPreferredWidth(100);
    }

    private void marcaItensTabela(boolean b) {
        for (int i = 0; i < modelTable.getRowCount(); i++) {
            modelTable.setValueAt(b, i, 0);
        }
    }

    private void GerarEtiquetas() {
        try {
            int linha = 0;
            switch (Integer.parseInt(jspColuna.getValue().toString())) {
                case 1:
                    linha = 0;
                    break;
                case 2:
                    linha = 12;
                    break;
                case 3:
                    linha = 24;
                    break;
                case 4:
                    linha = 36;
                    break;
                case 5:
                    linha = 48;
                    break;
            }

            linha += (Integer.parseInt(jspLinha.getValue().toString()) - 1);

            IfrGeraRelatorio ifr = new IfrGeraRelatorio();
            this.dispose();
            TelaInterna.add(ifr);

            ArrayList<Livro> lista = new ArrayList<>();
            for (int i = 0; i < linha; i++) {
                Livro l = new Livro();
                if (linha != 0) {
                    l.setTitulo("");
                    l.setSubtitulo("");

                    lista.add(l);
                }
            }

            for (int i = 0; i < modelTable.getRowCount(); i++) {
                Livro l = new Livro();
                if (Boolean.valueOf(modelTable.getValueAt(i, 0).toString())) {

                    l.setTitulo(modelTable.getValueAt(i, 2).toString());
                    l.setSubtitulo(modelTable.getValueAt(i, 7).toString());
                    l.setCodbarras(modelTable.getValueAt(i, 1).toString());
                    lista.add(l);
                }
            }
            
            ifr.visualizar("/relatorios/etiquetas.jasper", null, lista, ConstantesUtil.CAMINHO_SAIDA, this.getTitle());
            ifr.setVisible(true);
            ifr.setPosicao();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            LoggerUtil.log(ViewRetEtiquetasLivros.class,ex.getMessage());
        }
    }

