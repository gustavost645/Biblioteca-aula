    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (jTable1.getRowCount() != 0) {
            boolean check = false;
            for (int i = 0; i < modelTable.getRowCount(); i++) {
                if (Boolean.valueOf(modelTable.getValueAt(i, 0).toString())) {
                    check = true;
                }
            }
            if (check) {
                GerarEtiquetas();
            } else {
                JOptionPane.showMessageDialog(null, "Primeiro selecione algum livro para poder gerar a respectiva etiqueta!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Pesquise algum algum livro primeiro", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

