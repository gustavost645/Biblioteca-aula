    public void readJTable() {

        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setNumRows(0);
            LeitorDAO adao = new LeitorDAO();

            adao.consultarTodos().forEach((p) -> {
                modelo.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getMatricula(),
                    p.getTurma(),
                    p.getTurno()
                });
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(), "", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ListLeitor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void readJTableForDesc(String desc) {

        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setNumRows(0);
            LeitorDAO adao = new LeitorDAO();

            adao.consultar(desc).forEach((p) -> {
                modelo.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getMatricula(),
                    p.getTurma(),
                    p.getTurno()
                });
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(), "", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ListLeitor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

