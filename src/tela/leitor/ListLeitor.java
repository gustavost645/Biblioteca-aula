    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() != -1) {
            codAutor = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            
            
            DefaultTableModel ctm = (DefaultTableModel) jTable1.getModel();
            int linhaSelecionada = jTable1.getRowSorter().convertRowIndexToModel(jTable1.getSelectedRow());
            Leitor membro =  new Leitor();
            membro.setId(Integer.parseInt(ctm.getValueAt(linhaSelecionada, 0).toString()));
            membro.setNome(ctm.getValueAt(linhaSelecionada, 1).toString());
                      
            if (views instanceof IfrCadEmprestimo) {
                IfrCadEmprestimo lv = (IfrCadEmprestimo) views;
                lv.setMembro(membro);
                
                dispose();
            }
            
            if (views instanceof IfrCadRenovacao) {
                IfrCadRenovacao lv = (IfrCadRenovacao) views;
                lv.setMembro(membro);
                
                dispose();
            }
            
            if (viewsDialog instanceof ViewRetItensEmprestados) {
                ViewRetItensEmprestados lv = (ViewRetItensEmprestados) viewsDialog;
                lv.setMembro(membro);
                
                dispose();
            }
            
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

