    private void carregaDados() {
        try {
            ColecaoDAO adao = new ColecaoDAO();
            adao.consultarTodos().forEach((c) -> {
                comboColecoes.addItem((c.getId() + " - " + c.getDescricao()));
            });

            ClassLiterariaDAO dao = new ClassLiterariaDAO();
            dao.consultarTodos().forEach((c) -> {
                comboClassLiteraria.addItem((c.getId() + " - " + c.getDescricao()));
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(IfrCadLivros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void enviaCod(String codId) {
        txtNumExemplares.setEditable(false);
        txtNumExemplares.setEnabled(false);
        txtNumExemplares.setRequestFocusEnabled(false);

        try {
            Livro l = daoLivro.consultarId(Integer.parseInt(codId));
            DefaultTableModel jtAutor = (DefaultTableModel) jTlbAutor.getModel();
            DefaultTableModel jtAssunto = (DefaultTableModel) jTlbAssunto.getModel();
            jtAutor.setNumRows(0);
            jtAssunto.setNumRows(0);
            txtIDBook.setText(String.valueOf(l.getId()));
            txtTitulo.setText(l.getTitulo());
            txtSubtitulo.setText(l.getSubtitulo());
            txtDateAquisicao.setDate(l.getData_aquisicao());
            txtAnoEdicao.setText(String.valueOf(l.getAnoEdicao()));
            txtIsbn.setText(l.getIsbn());
            txtEdicao.setText(String.valueOf(l.getEdicao()));
            txtVolume.setText(String.valueOf(l.getVolume()));
            txtNumTombo.setText(l.getTombo());
            txtCDD.setText(l.getCdd());
            txtCDU.setText(l.getCdu());
            txtCutter.setText(l.getCutter());
            comboClassLiteraria.setSelectedItem(l.getClassLiteraria().getId());
            comboColecoes.setSelectedItem(l.getColecao().getId());
            checkEmprestimo.setSelected((l.getStatus() != 0));
            txtCodEditora.setText(l.getEditora().getId().toString());
            txtNomeEditora.setText(l.getEditora().getDescricao());
            //busca autores
            l.getAutor().forEach((a) -> {
                jtAutor.addRow(new Object[]{
                    a.getId(),
                    a.getNome()
                });
            });

            //busca assuntos
            l.getAssunto().forEach((s) -> {
                jtAssunto.addRow(new Object[]{
                    s.getId(),
                    s.getDescricao()
                });
            });

            this.txtTitulo.requestFocusInWindow();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(IfrCadLivros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

