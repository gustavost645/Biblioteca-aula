    private void txtCodEditoraFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodEditoraFocusLost
        if (!txtCodEditora.getText().trim().isEmpty()) {
            try {
                Editora ret = new EditoraDAO().consultarId(Integer.parseInt(txtCodEditora.getText().trim()));
                if (ret != null) {
                    setEditora(ret);

                } else {
                    JOptionPane.showMessageDialog(null, "Código da editora não localizado!\nfavor digite outro código", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    txtCodEditora.requestFocusInWindow();
                }
            } catch (HeadlessException | NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                LoggerUtil.log(IfrCadLivros.class, ex.getMessage());
            }
        }
    }//GEN-LAST:event_txtCodEditoraFocusLost

