    private void salvar() {

        if (jTlbAutor.getRowCount() != 0 && jTlbAssunto.getRowCount() != 0
                && !txtTitulo.getText().trim().equals("") && !txtCodEditora.getText().trim().equals("")
                && txtDateAquisicao.getDate() != null && !txtAnoEdicao.getText().trim().equals("")
                && !txtNumExemplares.getText().trim().equals("") && !txtVolume.getText().trim().equals("")
                && !txtEdicao.getText().trim().equals("")) {
            try {
                for (int i = 0; i < jTlbAutor.getRowCount(); i++) {
                    Autor s = new Autor();
                    s.setId(Integer.parseInt(jTlbAutor.getValueAt(i, 0).toString()));
                    s.setNome(jTlbAutor.getValueAt(i, 1).toString());
                    livro.addAutor(s);
                }

                for (int i = 0; i < jTlbAssunto.getRowCount(); i++) {
                    Assunto s = new Assunto();
                    s.setId(Integer.parseInt(jTlbAssunto.getValueAt(i, 0).toString()));
                    s.setDescricao(jTlbAssunto.getValueAt(i, 1).toString());
                    livro.addAssunto(s);
                }

                for (int i = 0; i < Integer.parseInt(txtNumExemplares.getText().trim()); i++) {
                    livro.setTitulo(txtTitulo.getText().trim());
                    livro.setSubtitulo(txtSubtitulo.getText().trim());
                    livro.setIsbn(txtIsbn.getText().trim().trim());
                    livro.setVolume(Integer.parseInt(txtVolume.getText().trim()));
                    livro.setAnoEdicao(Integer.parseInt(txtAnoEdicao.getText().trim()));
                    livro.setEdicao(Integer.parseInt(txtEdicao.getText().trim()));
                    livro.setTombo(txtNumTombo.getText().trim());
                    livro.setCdu(txtCDU.getText().trim());
                    livro.setCdd(txtCDD.getText().trim());
                    livro.setCutter(txtCutter.getText().trim());
                    livro.setEditora(new EditoraDAO().consultarId(Integer.parseInt(txtCodEditora.getText().trim())));
                    livro.setClassLiteraria(new ClassLiterariaDAO().consultarId(Integer.parseInt(comboClassLiteraria.getSelectedItem().toString().substring(0, (comboClassLiteraria.getSelectedItem().toString().split("-").length)).trim())));
                    livro.setColecao(new ColecaoDAO().consultarId(Integer.parseInt(comboColecoes.getSelectedItem().toString().substring(0, comboColecoes.getSelectedItem().toString().split("-").length).trim())));
                    livro.setData_aquisicao(new java.sql.Date(txtDateAquisicao.getDate().getTime()));
                    livro.setStatus(checkEmprestimo.isSelected()?1:0);

                    daoLivro.salvar(livro);
                }
                JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (HeadlessException | NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(IfrCadLivros.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            txtTitulo.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Favor verificar se todos os campos na cor cinza estão preenchidos!\nou se ha autores e assuntos adicionados!", "", JOptionPane.ERROR_MESSAGE);
        }
    }

